def zh_label(key: str) -> str:
    if key in I18N_ZH_EXACT:
        return I18N_ZH_EXACT[key]
    if key in I18N_ZH_PHRASES:
        return I18N_ZH_PHRASES[key]
    if key in I18N_ZH_ENUM:
        return I18N_ZH_ENUM[key]
    if key in I18N_ZH_RISK:
        return I18N_ZH_RISK[key]
    parts = key.replace("-", "_").split("_")
    out = []
    for part in parts:
        out.append(I18N_ZH_TOKEN.get(part, part))
    return "".join(out)
